package homeworks.advertising.enums;

/**
 * A type of browser,
 * Browser is an enum representing the most used type of browsers.
 *
 * @since 1.11
 */
public enum Browser {

    /** the singleton instance for the Browser FireFox Mozilla */
    MOZILLA,

    /** the singleton instance for the Browser Google Chrome */
    CHROME,

    /** the singleton instance for the Browser Opera */
    OPERA,

    /** the singleton instance for the Browser Yandex */
    YANDEX,

    /** the singleton instance for the Browser Safari, used only with OS MacOS */
    SAFARI
}
