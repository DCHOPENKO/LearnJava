package homeworks.advertising.enums;

/**
 * A type of Operation System,
 * SystemType is an enum representing the most used type of OS.
 *
 * @since 1.11
 */
public enum SystemType {

    /** the singleton instance for the SystemType Microsoft Windows */
    WINDOWS,

    /** the singleton instance for the SystemType Linux */
    LINUX,

    /** the singleton instance for the SystemType Apple MacOS */
    MACOS
}
